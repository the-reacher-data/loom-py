class NoopRunObserver:
    """Observer that discards all events.  Default when no observer is injected."""

    def on_pipeline_start(self, plan: Any, params: Any, run_id: str) -> None:
        pass

    def on_pipeline_end(self, run_id: str, status: RunStatus, duration_ms: int) -> None:
        pass

    def on_process_start(self, plan: Any, run_id: str, process_run_id: str) -> None:
        pass

    def on_process_end(self, process_run_id: str, status: RunStatus, duration_ms: int) -> None:
        pass

    def on_step_start(self, plan: Any, run_id: str, step_run_id: str) -> None:
        pass

    def on_step_end(
        self,
        step_run_id: str,
        status: RunStatus,
        rows_read: int,
        rows_written: int,
        duration_ms: int,
    ) -> None:
        pass

    def on_step_error(self, step_run_id: str, exc: Exception) -> None:
        pass


class LoggingRunObserver:
    """Observer that emits structured key=value log lines via stdlib ``logging``.

    All events go to the ``loom.etl`` logger at INFO level (errors at ERROR).
    Wire a JSON formatter in production to make these machine-parseable.

    Args:
        logger: Override the default ``loom.etl`` logger if needed.

    Example::

        import logging
        logging.basicConfig(level=logging.INFO)
        executor = ETLExecutor(reader, writer, observer=LoggingRunObserver())
    """

    def __init__(self, logger: logging.Logger | None = None) -> None:
        self._log = logger or _log

    def on_pipeline_start(self, plan: Any, params: Any, run_id: str) -> None:
        self._log.info(
            "pipeline_start pipeline=%s run_id=%s",
            plan.pipeline_type.__name__,
            run_id,
        )

    def on_pipeline_end(self, run_id: str, status: RunStatus, duration_ms: int) -> None:
        self._log.info(
            "pipeline_end run_id=%s status=%s duration_ms=%d",
            run_id,
            status,
            duration_ms,
        )

    def on_process_start(self, plan: Any, run_id: str, process_run_id: str) -> None:
        self._log.info(
            "process_start process=%s run_id=%s process_run_id=%s",
            plan.process_type.__name__,
            run_id,
            process_run_id,
        )

    def on_process_end(self, process_run_id: str, status: RunStatus, duration_ms: int) -> None:
        self._log.info(
            "process_end process_run_id=%s status=%s duration_ms=%d",
            process_run_id,
            status,
            duration_ms,
        )

    def on_step_start(self, plan: Any, run_id: str, step_run_id: str) -> None:
        self._log.info(
            "step_start step=%s run_id=%s step_run_id=%s",
            plan.step_type.__name__,
            run_id,
            step_run_id,
        )

    def on_step_end(
        self,
        step_run_id: str,
        status: RunStatus,
        rows_read: int,
        rows_written: int,
        duration_ms: int,
    ) -> None:
        self._log.info(
            "step_end step_run_id=%s status=%s rows_read=%d rows_written=%d duration_ms=%d",
            step_run_id,
            status,
            rows_read,
            rows_written,
            duration_ms,
        )

    def on_step_error(self, step_run_id: str, exc: Exception) -> None:
        self._log.error(
            "step_error step_run_id=%s error=%r",
            step_run_id,
            exc,
        )


class CompositeRunObserver:
    """Fan-out observer that forwards every event to N observers simultaneously.

    Use this to combine destinations without choosing between them::

        observer = CompositeRunObserver(
            LoggingRunObserver(),
            DeltaRunObserver(writer),
            OTelRunObserver(endpoint="https://otel.company.com"),
        )

    Args:
        *observers: One or more :class:`ETLRunObserver` implementations.
    """

    def __init__(self, *observers: ETLRunObserver) -> None:
        self._observers = observers

    def on_pipeline_start(self, plan: Any, params: Any, run_id: str) -> None:
        for obs in self._observers:
            obs.on_pipeline_start(plan, params, run_id)

    def on_pipeline_end(self, run_id: str, status: RunStatus, duration_ms: int) -> None:
        for obs in self._observers:
            obs.on_pipeline_end(run_id, status, duration_ms)

    def on_process_start(self, plan: Any, run_id: str, process_run_id: str) -> None:
        for obs in self._observers:
            obs.on_process_start(plan, run_id, process_run_id)

    def on_process_end(self, process_run_id: str, status: RunStatus, duration_ms: int) -> None:
        for obs in self._observers:
            obs.on_process_end(process_run_id, status, duration_ms)

    def on_step_start(self, plan: Any, run_id: str, step_run_id: str) -> None:
        for obs in self._observers:
            obs.on_step_start(plan, run_id, step_run_id)

    def on_step_end(
        self,
        step_run_id: str,
        status: RunStatus,
        rows_read: int,
        rows_written: int,
        duration_ms: int,
    ) -> None:
        for obs in self._observers:
            obs.on_step_end(step_run_id, status, rows_read, rows_written, duration_ms)

    def on_step_error(self, step_run_id: str, exc: Exception) -> None:
        for obs in self._observers:
            obs.on_step_error(step_run_id, exc)
